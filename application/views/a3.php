                <iframe width="560" height="315" src="https://www.youtube.com/embed/4Fsah8AuMAo?si=7AvPoIGpG9e6euLg"></iframe>
                <p>
                    <i>"Nutrisi memainkan peran krusial dalam keberhasilan budidaya hidroponik"</i>
                </p>
                <p>
                    Pada tahun 2018, produksi sawi mencapai 61.047 hektar, namun mengalami penurunan menjadi 60.871 
                    hektar pada tahun 2019, berdasarkan data dari Badan Pusat Statistik pada tahun 2019. Penurunan 
                    produksi ini mengindikasikan perlunya peningkatan dalam sistem budidaya sawi. Salah satu solusi 
                    yang efektif untuk mengatasi keterbatasan lahan adalah dengan menerapkan sistem hidroponik.
                </p>
                <p>
                    Nutrisi memainkan peran krusial dalam keberhasilan budidaya hidroponik. Tanaman tidak dapat tumbuh 
                    secara optimal tanpa pasokan nutrisi yang cukup. Teknologi yang menggabungkan nutrisi organik dan 
                    anorganik telah menjadi praktik umum dalam pertanian hidroponik. Penggabungan ini telah terbukti 
                    dapat meningkatkan produktivitas pertanian serta mengurangi ketergantungan pada pupuk kimia yang 
                    berlebihan. Salah satu alternatif nutrisi organik yang aman dan bermanfaat adalah ekstrak pupuk 
                    kandang ayam, yang dihasilkan melalui fermentasi bahan organik dari kotoran ayam. Nutrisi ini 
                    mengandung unsur-unsur yang bermanfaat untuk pertumbuhan tanaman sawi hijau.
                </p>
                <p>
                    <b>Nutrisi dan Manfaat Ekstrak Pupuk Kandang Ayam</b>
                </p>
                <p>
                    Ekstrak pupuk kandang ayam adalah produk pupuk yang dihasilkan melalui fermentasi bahan organik dari 
                    kotoran ayam. Produk ini merupakan pilihan yang baik sebagai nutrisi organik dalam budidaya hidroponik, 
                    karena kandungan nutrisi yang tinggi. Kombinasi kotoran padat dan cair dari unggas dalam produksi 
                    pupuk ini membuatnya lebih kaya akan unsur hara dibandingkan dengan pupuk dari ternak lainnya.
                </p>
                <p>
                    Ekstrak pupuk kandang ayam dapat menyediakan unsur hara yang mendukung pertumbuhan tanaman. Pemberian 
                    POC (Pupuk Organik Cair) dari kotoran ayam telah terbukti meningkatkan biomassa tanaman secara lebih 
                    efisien dibandingkan dengan pupuk organik dari sumber lain (Nofrianil, 2021). Penggunaan mikroorganisme 
                    efektif (EM-4) dalam pembuatan ekstrak pupuk kandang ayam menghasilkan unsur hara yang dapat diserap 
                    langsung oleh akar tanaman. Pupuk organik cair kotoran ayam dengan dosis 30 ml per tanaman memberikan 
                    pengaruh positif terhadap pertumbuhan tanaman sawi dalam hal tinggi tanaman, jumlah daun, dan volume 
                    akar (Limbongan, 2015).
                </p>
                <p>
                    Kombinasi baru dalam bentuk ekstrak pupuk kandang ayam dengan nutrisi AB mix membawa inovasi dalam 
                    pertanian. Kombinasi ini memungkinkan penggabungan keunggulan dari berbagai teknologi yang ada, 
                    menghasilkan solusi yang lebih efisien dan efektif.
                </p>
                <p>
                    Penggunaan nutrisi AB mix sebesar 75% dengan tambahan ekstrak pupuk kandang ayam sebesar 25% adalah 
                    suatu langkah inovatif yang mampu mendukung pertumbuhan tanaman sawi setara dengan penggunaan nutrisi 
                    AB mix 100%. Hal ini mungkin disebabkan oleh kandungan unsur hara makro dalam kombinasi tersebut yang 
                    memenuhi kebutuhan nutrisi tanaman sawi. Unsur hara nitrogen (N) berperan penting dalam pembentukan 
                    sel dan pertumbuhan tanaman, sementara unsur fosfor (P) penting untuk aktivitas metabolisme dan 
                    pembelahan sel-sel meristem pada pucuk dan akar tanaman sawi (Purba dkk., 2021).
                </p>
                <p>
                    <b>Prospek Ekstrak Pupuk Kandang Ayam sebagai Nutrisi Hidroponik</b>
                </p>
                <p>
                    Kombinasi ekstrak pupuk kandang ayam dan nutrisi AB mix menyediakan nutrisi yang seimbang dan lengkap 
                    untuk tanaman. Ekstrak pupuk kandang ayam memberikan nutrisi organik yang bermanfaat, sementara AB mix 
                    menyediakan nutrisi anorganik yang mudah diserap oleh tanaman. Dengan demikian, tanaman mendapatkan 
                    nutrisi yang dibutuhkan untuk pertumbuhan optimal. Kombinasi ini juga membantu mengurangi ketergantungan 
                    pada pupuk kimia yang berlebihan, yang dapat berdampak negatif pada lingkungan dan kesehatan. Dengan 
                    demikian, nutrisi ini dapat diterapkan secara luas, baik untuk petani maupun masyarakat umum.
                </p>
                <p>
                    Penerapan nutrisi AB mix 75% dengan ekstrak pupuk kandang ayam 25% dalam budidaya sawi dapat mendukung 
                    pertanian berkelanjutan, sambil mengurangi risiko pencemaran lingkungan yang disebabkan oleh penggunaan 
                    pupuk kimia berlebihan.
                </p>
