                </p>
                <iframe width="560" height="315" src="https://www.youtube.com/embed/HV4Br-eYK8s?si=8KSxRM18iZWzqaql"></iframe>
                <br><br>
                <p>
                    Peranan nutrisi dalam budidaya hidroponik sangat vital untuk memenuhi kebutuhan pertumbuhan optimal 
                    tanaman. Jika jumlah nutrisi hidroponik tidak sesuai dengan kebutuhan tanaman, dapat berdampak negatif 
                    pada kualitas hasil panen. Meskipun larutan nutrisi AB mix populer dalam hidroponik, namun harganya 
                    cenderung tinggi dan terkadang sulit ditemukan di beberapa daerah. Oleh karena itu, diperlukan 
                    alternatif nutrisi yang lebih terjangkau, mudah didapatkan, dan tetap memberikan nutrisi yang 
                    diperlukan bagi pertumbuhan tanaman.
                </p>
                <p>
                    <b>Manfaat Daun Lamtoro</b>
                </p>
                <p>
                    Salah satu solusi adalah menggunakan ekstrak daun lamtoro sebagai pupuk organik dalam hidroponik. 
                    Daun lamtoro dapat diolah menjadi ekstrak terlebih dahulu untuk memastikan tanaman dapat menyerap 
                    unsur haranya. Daun lamtoro memiliki kandungan nutrisi yang cukup lengkap untuk pertumbuhan tanaman, 
                    seperti Nitrogen (3,84%), Fosfor (0,2%), Kalium (2,06%), Kalsium (1,31%), dan Magnesium (0,33%). 
                    Pemberian ekstrak daun lamtoro pada tanaman sawi telah terbukti meningkatkan pertumbuhan dan produksi, 
                    terutama dalam hal tinggi tanaman dan panjang perakaran (Windawati et.al, 2020). Komposisi khusus 
                    dari ekstrak daun lamtoro diharapkan dapat memenuhi kebutuhan pertumbuhan dan produksi tanaman sawi 
                    dalam budidaya hidroponik.
                </p>
                <p>
                    Manfaat dari penggunaan daun lamtoro dalam hidroponik sangatlah signifikan. Nutrisi yang digunakan 
                    dalam hidroponik sangat memengaruhi hasil budidaya. Nutrisi AB mix, yang terdiri dari stok A dan stok 
                    B, umumnya mahal dan sulit ditemukan di beberapa daerah. Oleh karena itu, menggantinya dengan nutrisi 
                    organik seperti ekstrak daun lamtoro menjadi alternatif yang lebih terjangkau dan dapat dengan mudah 
                    diperoleh oleh petani. Kandungan nutrisi yang tinggi dalam daun lamtoro, terutama Nitrogen, membuatnya 
                    menjadi pilihan yang baik sebagai sumber nutrisi organik. POC yang dihasilkan dari campuran daun lamtoro, 
                    daun bambu, sabut kelapa, dan bonggol pisang dalam konsentrasi 10,4 ml/l air/polybag telah terbukti 
                    memberikan hasil optimal pada tanaman sawi, termasuk berat basah, jumlah daun, dan tinggi tanaman 
                    (Wasilah dan Bashri, 2019).
                </p>
                <p>
                    Daun lamtoro memiliki kandungan nutrisi yang sangat bermanfaat untuk pertumbuhan tanaman saat diaplikasikan 
                    sebagai sumber nutrisi organik. Kandungan ini mencakup 4,2% Nitrogen, 0,23% Fosfor, 2,36% Kalsium, 
                    4% Tannin, 7,19% Mimosin, 25,9% protein kasar, dan 40% Karbohidrat (Devi et.al, 2013). Penggunaan 
                    ekstrak daun lamtoro diharapkan dapat melengkapi atau menggantikan AB mix dalam memenuhi kebutuhan 
                    nutrisi tanaman sawi dalam budidaya hidroponik.
                </p>
                <p>
                    <b>Ekstrak lamtoro sebagai alternatif hara</b>
                </p>
                <p>
                    Namun, perlu diingat bahwa ekstrak daun lamtoro belum dapat menggantikan sepenuhnya nutrisi AB mix sebagai 
                    sumber nutrisi hidroponik. Hal ini kemungkinan karena kandungan nutrisi dalam ekstrak daun lamtoro lebih 
                    rendah dibandingkan dengan AB mix. AB mix mengandung banyak unsur hara makro seperti Nitrogen (207,2 ppm), 
                    Fosfor (51,2 ppm), Kalium (24,96 ppm), Kalsium (144,8 ppm), dan Magnesium (52,8 ppm) (AB Mix Kemasan, 2022). 
                    Perbedaan dalam nutrisi yang disediakan oleh ekstrak daun lamtoro dan AB mix mengakibatkan tanaman sawi yang 
                    menggunakan AB mix memiliki pertumbuhan yang lebih baik dibandingkan dengan yang menggunakan ekstrak daun 
                    lamtoro. Tanaman sawi yang mendapatkan nutrisi dari ekstrak daun lamtoro mungkin mengalami kekurangan 
                    Nitrogen, yang dapat memengaruhi pertumbuhan dan produksi tanaman sawi, terutama pada bagian vegetatif.
                </p>
                <p>
                    Untuk memaksimalkan hasil, perlu dipertimbangkan komposisi optimal dari ekstrak daun lamtoro sebagai 
                    alternatif nutrisi dalam budidaya hidroponik. Dalam penelitian ini, komposisi 25% ekstrak daun lamtoro 
                    tampaknya merupakan batas maksimal yang dianjurkan sebagai substitusi nutrisi AB mix. Konsentrasi yang 
                    lebih tinggi dari ekstrak daun lamtoro dapat mengakibatkan penurunan pertumbuhan dan produksi tanaman 
                    sawi. Oleh karena itu, perlu mempertimbangkan penambahan nutrisi organik lain yang lebih kaya akan unsur 
                    hara untuk memenuhi kebutuhan tanaman secara optimal.
                </p>
